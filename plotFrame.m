function plotFrame(T,lambda)
box on;grid on;hold on;axis equal;
plot3([T(1,4) T(1,4)+lambda*T(1,1)],[T(2,4) T(2,4)+lambda*T(2,1)],...
    [T(3,4) T(3,4)+lambda*T(3,1)],'r','linewidth',2);
plot3([T(1,4) T(1,4)+lambda*T(1,2)],[T(2,4) T(2,4)+lambda*T(2,2)],...
    [T(3,4) T(3,4)+lambda*T(3,2)],'g','linewidth',2);
plot3([T(1,4) T(1,4)+lambda*T(1,3)],[T(2,4) T(2,4)+lambda*T(2,3)],...
    [T(3,4) T(3,4)+lambda*T(3,3)],'b','linewidth',2);
end